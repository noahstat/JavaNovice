public class Assignoperator {
    public static void main(String[] args){
        int a = 40;
        double f = 3.45;
                System.out.println("The value of a is:"+a);
                System.out.println("The value of f is:"+f);
            a += 10;
                System.out.println("The value of addition assignment operator 'a +=10' value is:"+a);
            a -= 5;
                System.out.println("The value of subtraction assignment operator 'a -=5' is:"+a);
            f *= 5.00;
                System.out.println("The value of multiplication assignment operator 'f *=5.00' is:"+f);
            a /= 5;
                System.out.println("The value of division assignment operator 'a /=5' value is:"+a);
            f %= 2;
                System.out.println("The value of modulus assignment operator 'f %=2' value is:"+f);
    }
}
